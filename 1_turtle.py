import turtle as t
t.fd(100)
t.right(120)
t.fd(100)
t.right(120)
t.fd(100)
t.up()
t.goto(0,-150)
t.down()
t.left(150)
t.fd(100)
t.left(90)
t.fd(100)
t.left(90)
t.fd(100)
t.left(90)
t.fd(100)
t.done()