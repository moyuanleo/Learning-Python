'''------------------------------------------------------
【程序设计】
---------------------------------------------------------
题目：获得输入正整数 N，判断 N 是否为质数，如果是则输出 True，
      否则输出 False。本题不考虑输入异常情况。 

输出实例：
24
False
---------------------------------------------------------
注意：部分源程序给出如下。请勿改动其它函数中的任何内容，
      仅在函数的注释标志之间填入所编写的若干语句。
------------------------------------------------------'''

#**********Program**********
N=eval(input())
flag=True
for i in range(2,N):
    if N%i==0:
        flag=False
        break
print(flag)






