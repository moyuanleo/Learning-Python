'''------------------------------------------------------
【程序设计】
---------------------------------------------------------

题目：今年多少天？

描述：闰年366天，其他年份365天。普通年（不能被100整除的年份）能被4整除的为闰年。
     （如2004年就是闰年,1999年不是闰年）；
      世纪年（能被100整除的年份）能被400整除的是闰年。(如2000年是闰年，1900年不
                                                       是闰年)；
      用户输入一个正整数，代表年份，输出该年有多少天？ 

要求：

     输入格式：输入一个正整数，表示年份


     输出格式：该年的天数



      输入示例    输出示例

 示例1  1999        365
 示例2  2000        366
---------------------------------------------------------
注意：仅在注释标志之间填入所编写语句。
------------------------------------------------------'''

#**********Program**********
year==int(input())
if year%400==0 or (year%4==0 and year %100!=0):
     print(366)
else:
     print(365)




