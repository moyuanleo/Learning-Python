import turtle as t
t.fd(100)
t.seth(240)
t.fd(100)
t.seth(120)
t.fd(100)
t.seth(270)
t.up()
t.fd(150)
t.down()
t.fd(100)
t.seth(360)
t.fd(100)
t.seth(90)
t.fd(100)
t.seth(180)
t.fd(100)
t.done()